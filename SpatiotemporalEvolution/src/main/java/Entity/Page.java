package Entity;

public class Page {
    public String name;
    public String url;
    public String descript;

    public void setName(String pagename){ this.name = pagename; }
    public String getName(){ return this.name; }

    public void setUrl(String pageurl){ this.url = pageurl; }
    public String getUrl(){ return this.url; }

    public void setDescript(String descripts){ this.url = descripts; }
    public String getDescript(){ return this.descript; }
}
